class Season {
  final int id;
  final String name;
  final String overview;
  final String posterPath;
  final int seasonNumber;
  final int episodeCount;

  Season({
    required this.id,
    required this.name,
    required this.overview,
    required this.posterPath,
    required this.seasonNumber,
    required this.episodeCount,
  });
}
