class Episode {
  final int id;
  final String name;
  final String overview;
  final String airDate;
  final int episodeNumber;
  final String stillPath;

  Episode({
    required this.id,
    required this.name,
    required this.overview,
    required this.airDate,
    required this.episodeNumber,
    required this.stillPath,
  });
}
